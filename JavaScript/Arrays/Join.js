// O join()método retorna um array como uma string.

// O join()método não altera a matriz original.

// Qualquer separador pode ser especificado. O padrão é vírgula (,).

let arr = ["ovo", "farinha", "feijão", "arroz"];

let compras = arr.join(" "); // se não colocar nada ele separa por vírgula

console.log(compras);
