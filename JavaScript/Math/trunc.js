// O método Math.trunc() retorna a parte inteira de um número.

// Nota: Este método NÃO arredondará o número para cima/para baixo para o inteiro mais próximo, mas simplesmente removerá os decimais.

let n1 = Math.random() * 10 + 1; // aqui imprime um número até 10

console.log(n1);
console.log(Math.trunc(n1));
